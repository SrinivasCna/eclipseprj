package basics.ch_05_datatypes;

public class DatatypesDemo1 {

	public static void main(String[] args) {
		// single character
		char character = 'Z';
		System.out.println(character);

		// set of characters
		String subject = "java";
		System.out.println(subject);
		// whole numbers
		// byte, int,short, long

		// whole numbers
		byte byteNumber = 123;
		System.out.println(byteNumber);

		// short
		short sNumber = 32767;
		System.out.println(sNumber);

		// int number
		int intNumber = 1_234_567;
		System.out.println(intNumber);

		// long Number
		long l = 1_234_567_891L;
		System.out.println(l);

		// decimal number => float, double
		float f = 1.004562F;
		System.out.println(f);

		double d = 100.1545454544555;
		System.out.println(d);

		// boolean values
		boolean t = true;
		System.out.println(t);

	}

}
